				<h1 class="p">Declaración de Misión del CARCCSAM</h1>
				<p> 

<p class="b"><b>Los miembros del Consejo de Ansianos (CA) de la Renovación Carismática Católica de Santaigo de Maria, se comprometen a:
<br/>
1. Servir a Dios en la Iglesia Católica, en sumisión primordial de evangelización en el poder del Espíritu Santo.
<br/>
2. Promover y fomentar la experiencia de "Pentecostés" como gracia actual del Espíritu Santo para la Iglesia de nuestro tiempo.
<br/>
3. Motivar y llevar el fervor de la Renovación en el Espíritu Santo a todos los ambientes donde, vivimos y compartimos como Iglesia de Cristo. Esto se realizará en nuestras familias, vecindad y comunidad parroquial, entre todos los que tienen sed de Dios y necesitan el toque vivificador de Cristo por medio de su Espíritu.
<br/>
<br/>
Este compromiso se alcanzará:
<br/>
a. Mediante nuestro testimonio de vida.
<br/>
b. Proclamando la Buena Nueva.
<br/>
c. Fomentando la organización y formación de Comunidades y/o Grupos de Oración.
<br/>
d. Viviendo un verdadero compromiso en los diversos apostolados de la Iglesia.
<br/>
e. Promoviendo y realizando planes de evangelización bajo la guía del Espíritu Santo.
<br/>
f. Produciendo materiales de Formación y utilizando los medios de comunicación para impactar, a través de la Iglesia, al mundo y sociedad en que vivimos.
<br/>
4. Promover la unidad y comunicación dentro de la Renovación Carismática Católica (RCC) a nivel local y con los diversos movimientos y apostolados de la Iglesia, fomentando el ecumenismo y la unidad del Cuerpo de Cristo bajo la dirección de la Iglesia Católica.
<br/>
5. Implementar un plan de trabajo que facilite el desarrollo de la RCC mediante la:
<br/>
 a. Realización del Encuentro Católico Carismático, como Asamblea semanales que congregue y unifique a los miembros  de  RCC.
<br/>
b. Organización de Seminarios de Vida en el Espíritu Santo, Seminarios de Formación de Líderes y otras áreas de especialidad en el campo de la Renovación.
<br/>
c. Conferencias.</b></p>

